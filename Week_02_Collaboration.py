#Square area
print("Rectangle area calculation")
side = float(input("Insert side length: "))
Area = (side * side)
print(f"The area of the square is: {Area}")

#Rectangle area
print("Rectangle area calculation")
Width = float(input("Insert width: "))
Height = float(input("Insert height: "))
Area = (Width * Height)
print(f"The area of the rectangle is: {Area}")

#Circle area
from math import pi
print("circle area calculation")
Radius = float(input("Insert radius: "))
Area = pi * Radius ** 2
print(f"The area of the rectangle is: {Area}")